/* ============================================================
 * Русские аналоги функций вывода
 * ============================================================ */


/* принт - аналог sys_write */
int печать(int fd, char *buf, int count) {
    return __syscall(1, fd, buf, count, 0, 0);
}


/* длина_строки - аналог strlen */
int длина_строки(char *s) {
    int n = 0;
    while (*s) { n += 1; s = s + 1; }
    return n;
}
